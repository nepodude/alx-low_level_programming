#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - will see positivity or negativity of integer n
 * and if it is equal to 0 too.
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	if (n > 0)
		printf("%d is positive\n", n);
	else if (n == 0)
		printf("%d is zero\n", n);
	else if (n < 0)
		printf("%d is negative\n", n);
	return (0);
}
